import { IsEnum, IsIn, IsNotEmpty, IsNumber, IsString } from 'class-validator';
import { ROLES } from '../../../common/constants';

export class CreateUserDto {
  @IsNotEmpty()
  @IsString()
  firstName: string;

  @IsNotEmpty()
  @IsString()
  lastName: string;

  @IsNotEmpty()
  @IsNumber()
  age: number;

  @IsNotEmpty()
  @IsString()
  email: string;

  @IsNotEmpty()
  @IsString()
  username: string;

  @IsNotEmpty()
  @IsString()
  password: string;

  @IsNotEmpty()
  @IsEnum(ROLES)
  @IsIn(Object.values(ROLES))
  role: ROLES;
}
